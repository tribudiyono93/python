List = ["Geeks", "For", "Geeks"]
print("Accesing a element from the list : ")
print(List[0])
print(List[2])

List = [['Geeks', 'For'], ['Geeks']]
print("Accesing a element from a multi-dimensional list")
print(List[0][1])
print(List[1][0])

List = [1, 2, 'Geeks', 4, 'For', 6, 'Geeks']
print("Accessing element using negative indexing")
print(List[-1])
print(List[-2])
print(List[-3])
print(List[-4])