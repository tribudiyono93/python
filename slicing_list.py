List = ['G', 'E', 'E', 'K', 'S', 'F', 'O', 'R', 'G', 'E', 'E', 'K', 'S']
print("Initial list")
print(List)

Sliced_List = List[3:8]
print("\nSlicing elements in a range 3-8")
print(Sliced_List)

Sliced_List = List[:-6]
print("\nElement sliced till 6th element from last")
print(Sliced_List)

Sliced_List = List[5:]
print("\nElement sliced from 5th element till the end :")
print(Sliced_List)

Sliced_List = List[:]
print("\nPrinting all elements using slice operation")
print(Sliced_List)

Sliced_List = List[::-1]
print("\nPrinting list in reverse : ")
print(Sliced_List)