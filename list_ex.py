List = [['Geeks', 'For'], ['Geeks']]
print("\nMulti-Dimensional List : ")
print(List)

List = [1,2,4,4,3,3,3,6,5]
print("\nList with the use of numbers")
print(List)

List = [1,2, 'Geeks', 4, 'For', 6, 'Geeks']
